/**
 * @brief  获取字符的字模数据
 * @param  ch: ASCII 字符
 * @retval 5 字节字模指针, 不支持的字符返回空格
 */
static inline const uint8_t* Font_GetChar(char ch)
{
    if (ch < 0x20 || ch > 0x7E) {
        return FONT_5x7[0];  /* 空格 */
    }
    return FONT_5x7[ch - 0x20];
}

